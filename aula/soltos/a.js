let a = 5;
let b = "5";

//comparaçao frouxa (==)
if (a == b) {
    console.log ("a == b: São iguais!");
} else {
    console.log("a == b: São diferentes!");
}

//comparação estrita (===)
if (a === b) {
    console.log("a === b: São iguais!");
} else {
    console.log("a === b: São diferentes!");
}


